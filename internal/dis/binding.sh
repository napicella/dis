# Add instructions that must be executed during bash initialization.
#
# Example usage:
# bashrc_init_add "Autojump" '[[ -s ~/.autojump/etc/profile.d/autojump.sh ]] && source ~/.autojump/etc/profile.d/autojump.sh'
function bashrc_init_add() {
  local SEARCH_STRING="# ${1} import generated by dis config"
  local CONFIG=$2
  local BASH_INIT="$HOME/rc/configs-generated/bash_init"

  if ! grep -qF "$SEARCH_STRING" "$BASH_INIT"; then
